int16u mb_crc16( int8u * data, int16u len )
{
    int8u           crc_hi = 0xFF;
    int8u           crc_lo = 0xFF;
    int             i;

    while( len-- )
    {
        i = crc_lo ^ *( data++ );
        crc_lo = ( UCHAR )( crc_hi ^ mb_crc_hi[i] );
        crc_hi = mb_crc_lo[i];
    }
    return ( int16u )( crc_hi << 8 | crc_lo );
}


