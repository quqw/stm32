int32u  parse_u32(String pdata,int8u size)
{
    int32u ret=0,temp=0;
    int8u  i;

    for(i=0;i<size;i++){
        if((pdata[i]>='0')&&(pdata[i]<='9'))
		{
            temp=(pdata[i]-'0');
        }
		else  if((pdata[i]>='A')&&(pdata[i]<='F'))
		{
            temp=(pdata[i]-'A'+10);
        }
		else if((pdata[i]>='a')&&(pdata[i]<='f'))
		{
            temp=(pdata[i]-'a'+10);;
        }
		else
			break;
        ret=10*ret+temp;
    }
    return ret;
}

